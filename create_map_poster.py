#!/usr/bin/env python3
"""
City Map Poster Generator - CLI Wrapper

This is a thin command-line wrapper around the app.services.map_generator module.
All map generation logic has been moved to app/services/map_generator.py for
better integration with the web application.

For backward compatibility, this script maintains the same CLI interface.
"""

import os
import sys
import argparse

# Import from the service module
from app.services.map_generator import (
    get_coordinates,
    load_theme,
    fetch_map_data,
    render_poster,
    generate_output_filename,
    get_available_themes
)


